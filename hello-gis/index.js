function resize(array, calculation) {
    let resized = array.map( (el) => {
        if (el.constructor === Array) {
            return resize(el, calculation)
        } else {
            return calculation(el)
        }
    })
    return resized
}

function move(array, movement) {
    let moved = array.map( dim0 => {
        return dim0.map( dim1 => {
            return movement(dim1)
        })
    })
    return moved
}

